import { json } from "@remix-run/node";
import { useLoaderData } from "@remix-run/react";

import oasDocument from "~/specs/githubv3.json";
import type { OpenAPIV3 } from "openapi-types";
import { getAuthorizationUrlForScheme } from "~/specs/utils";

export async function loader() {
  const authorizeUrl = getAuthorizationUrlForScheme(
    oasDocument as OpenAPIV3.Document,
    "oauth2",
    ["repos/get"]
  );

  authorizeUrl?.searchParams.set(
    "client_id",
    process.env.OAUTH_CLIENT_ID ?? ""
  );

  return json({
    authorizeUrl: authorizeUrl?.href,
  });
}

export default function Index() {
  const data = useLoaderData<typeof loader>();

  return (
    <main className="relative min-h-screen bg-white sm:flex sm:items-center sm:justify-center">
      <div className="relative sm:pb-16 sm:pt-8">
        <div className="mx-auto max-w-7xl sm:px-6 lg:px-8">
          <div className="relative shadow-xl sm:overflow-hidden sm:rounded-2xl">
            <div className="absolute inset-0">
              <img
                className="h-full w-full object-cover"
                src="https://user-images.githubusercontent.com/1500684/157774694-99820c51-8165-4908-a031-34fc371ac0d6.jpg"
                alt="Sonic Youth On Stage"
              />
              <div className="absolute inset-0 bg-[color:rgba(254,204,27,0.5)] mix-blend-multiply" />
            </div>
            <div className="relative px-4 pt-16 pb-8 sm:px-6 sm:pt-24 sm:pb-14 lg:px-8 lg:pb-20 lg:pt-32">
              <h1 className="text-center text-6xl font-extrabold tracking-tight sm:text-8xl lg:text-9xl">
                <span className="block uppercase text-yellow-500 drop-shadow-md">
                  Indie Stack
                </span>
              </h1>
              <p className="mx-auto mt-6 max-w-lg text-center text-xl text-white sm:max-w-3xl">
                Check the README.md file for instructions on how to get this
                project deployed.
              </p>
              <div className="mx-auto mt-10 max-w-sm sm:flex sm:max-w-none sm:justify-center">
                {data.authorizeUrl ? (
                  <a
                    href={data.authorizeUrl}
                    className="flex items-center justify-center rounded-md border border-transparent bg-white px-4 py-3 text-base font-medium text-yellow-700 shadow-sm hover:bg-yellow-50 sm:px-8"
                  >
                    Authorize OAuth Client using OpenAPI Spec
                  </a>
                ) : (
                  <h3 className="text-red-500">
                    Make sure to set the OAUTH_CLIENT_ID env var
                  </h3>
                )}
              </div>
              <a href="https://remix.run">
                <img
                  src="https://user-images.githubusercontent.com/1500684/158298926-e45dafff-3544-4b69-96d6-d3bcc33fc76a.svg"
                  alt="Remix"
                  className="mx-auto mt-16 w-full max-w-[12rem] md:max-w-[16rem]"
                />
              </a>
            </div>
          </div>
        </div>
      </div>
    </main>
  );
}
